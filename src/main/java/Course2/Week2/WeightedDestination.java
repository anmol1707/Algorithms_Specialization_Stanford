package Course2.Week2;

public class WeightedDestination {
    private int vertexId;
    private int weight;

    public WeightedDestination(int vertexId, int weight) {
        this.vertexId = vertexId;
        this.weight = weight;
    }

    public int getVertexId() {
        return vertexId;
    }

    public int getWeight() {
        return weight;
    }

}
